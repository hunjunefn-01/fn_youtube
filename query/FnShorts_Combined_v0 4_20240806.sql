DECLARE @threeMonthsBefore DATE;
SELECT TOP 1 @threeMonthsBefore = DT FROM WISE.DBO.TZ_DATE WHERE TRADE_YN = '1' AND DT <= DATEADD(month, -3, @endDate) ORDER BY DT DESC
--SELECT @threeMonthsBefore


SELECT a.* 
, c.RECOM_TYP_DT
, c.RECOM_TYP_AVG
, c.RECOM_TYP_MAX  
, c.RECOM_TYP_MIN  
, c.RECOM_TYP_MID  
, c.TARGET_PRC_AVG 
, d.VAL_AVG AS 'TARGET_PRC_AVG_3_MONTHS_BEFORE'
, c.TARGET_PRC_MAX 
, c.TARGET_PRC_MIN 
, c.TARGET_PRC_MID 
, c.TARGET_PRC_DIFF
, c.TARGET_PRC_CHG 
, c.TARGET_PRC_DT 
, c.RECOM_TYP_CNT 
, c.TARGET_PRC_CNT
, c.TARGET_PRC_UP_1W
, c.TARGET_PRC_DOWN_1W
, c.TARGET_PRC_HOLD_1W
, c.TARGET_PRC_NEW_1W 
, c.TARGET_PRC_UP_DOWN_1W
FROM #TempTable a
	LEFT OUTER JOIN #TMP_CNS_LIST c
		ON a.STK_CD = c.CMP_CD 
	LEFT OUTER JOIN WISE.DBO.TT_CMP_CNS_EXT_DATA3 d
		on a.STK_CD = d.CMP_CD
		AND d.ITEM_CD = '761250000'--목표주가(adj)
		AND d.CNS_DT = @threeMonthsBefore






